    private void tblUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUserMouseClicked
        // TODO add your handling code here:
        //Menampilkan data kedalam form pengisian:
        int baris = tblUser.rowAtPoint(evt.getPoint());
        
        String id_user = tblUser.getValueAt(baris, 1).toString();
        String nama_user = tblUser.getValueAt(baris, 2).toString();
        String username_user = tblUser.getValueAt(baris, 3).toString();
        String password = tblUser.getValueAt(baris, 4).toString();
        
        tbIdUser.setText(id_user);
        tbNameReg.setText(nama_user);
        tbUsernameReg.setText(username_user);
        tbPasswordReg.setText(password);
        
        if (tblUser.getValueAt(baris, 5).toString().equals("0")) {
            radioAdmin.setSelected(true);
        } else if (tblUser.getValueAt(baris, 5).toString().equals("1")) {
            radioManajer.setSelected(true);
        } else if (tblUser.getValueAt(baris, 5).toString().equals("2")) {
            radioKasir.setSelected(true);
        }
    }//GEN-LAST:event_tblUserMouseClicked

