    private void btnSimpanUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSimpanUserMouseClicked
        // TODO add your handling code here:
        String nama = tbNameReg.getText();
        String username = tbUsernameReg.getText();
        String password = tbPasswordReg.getText();
        
        String role_id = "null";
                if (radioAdmin.isSelected()) {
                    role_id = "0";
                } else if(radioManajer.isSelected()) {
                    role_id = "1";
                } else if(radioKasir.isSelected()) {
                    role_id = "2";
                }
                
        if (nama.equals("")) {
            JOptionPane.showMessageDialog(null, "Masukkan nama User terlebih dahulu");
        } else if (username.equals("")){
            JOptionPane.showMessageDialog(null, "Masukkan username User terlebih dahulu");
        } else if (password.equals("")) {
            JOptionPane.showMessageDialog(null, "Masukkan password User terlebih dahulu");
        } else if (role_id.equals("null")) {
            JOptionPane.showMessageDialog(null, "Pilih jabatan User terlebih dahulu");
        } else {
            try {
                sql = "INSERT INTO user VALUES (NULL, '" + nama + "', '" + username + "', '" + 
                        password + "', '" + role_id + "')";
                pst = conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                load_table();
                bersihkan();
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_btnSimpanUserMouseClicked

    private void btnEdituserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEdituserMouseClicked
        // TODO add your handling code here:
        String idUser = tbIdUser.getText();
        if (idUser.equals("")) {
            JOptionPane.showMessageDialog(null, "Pilih User yang ingin diubah");
        } else {
            try {
                int role_id = 0;
                if (radioAdmin.isSelected()) {
                    role_id = 0;
                } else if(radioManajer.isSelected()) {
                    role_id = 1;
                } else if(radioKasir.isSelected()) {
                    role_id = 2;
                }

                sql = "UPDATE user SET nama_user = '" + tbNameReg.getText() + "', username = '" + 
                        tbUsernameReg.getText() + "', password = '" + tbPasswordReg.getText() + 
                        "', role_id = '" + role_id + "' WHERE id_user =" + idUser;
                pst = conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                load_table();
                bersihkan();
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_btnEdituserMouseClicked

